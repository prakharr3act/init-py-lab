import math

print(math.pi)
print(math.e) # e is the exponential constant, approximately equal to 2.71828
print(math.sqrt(16)) # square root of 16 is 4
print(math.ceil(3.7)) # ceiling of 3.7 is 4
# ceil returns the smallest integer greater than or equal to the given number || round up
print(math.floor(3.7)) # floor of 3.7 is 3
# floor returns the largest integer less than or equal to the given number || round down
