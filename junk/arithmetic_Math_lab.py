# Round( )function
y = 12
z = 21.032
print(round(y)) # output: 12
print(round(z)) # output: 21

# Absolute( ) function 
# distance away from the whole number
v = -12
print(abs(v)) # output: 12

# power( ) function
w = pow(2,3) # 2 is base and 3 is exponent 
# similar as we use in js 2**3
print(w) # output: 8

# max( ) function
# helps to find the maximum number from the given numbers
a = 1.026
b = 3.021
c = 120.20                       # It is not a good practice to assign variables name like a b c d.
                                 # It is better to assign meaningful variable names.
d = 0.0001
print(max(a,b,c,d)) # output: 120.2

# min  ( ) function
# helps to find the minimum number from the given numbers

e = 1.026
f = 3.021
g = 120.20
h = 0.0001
print(min(e,f,g,h)) # output: 0.0001

